export const MESSAGES: Record<Locale, Dict> = { en, fr, de, es, pt };

export function isLocale(value: string): value is Locale {
  return (LOCALES as readonly string[]).includes(value);
}

export type MsgParams = Record<string, string | number>;

/** Replace `{name}` placeholders with values from `params` (unknown keys kept). */
export function interpolate(template: string, params?: MsgParams): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (m, k) =>
    params[k] !== undefined ? String(params[k]) : m,
  );
}

/** Translate `key` for `locale`, falling back to English then the key itself. */
export function translate(
  locale: Locale,
  key: string,
  params?: MsgParams,
): string {
  return interpolate(
    MESSAGES[locale]?.[key] ?? MESSAGES.en[key] ?? key,
    params,
  );
}
